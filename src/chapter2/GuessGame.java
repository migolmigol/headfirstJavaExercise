package chapter2;

public class GuessGame {
	
	Player p1;
	Player p2;
	Player p3;
	
	public void startGame() {
		p1 = new Player();
		p2 = new Player();
		p3 = new Player();
		
		int guessp1 = 0;
		int guessp2 = 0;
		int guessp3 = 0;
		
		boolean p1isRight = false;
		boolean p2isRight = false;
		boolean p3isRight = false;
		
		int targetNumber = (int) (Math.random() * 10);
		System.out.println("0 이상 9 이하의 숫자를 맞춰보세요.");
		log(" ");
		
		int loopCount = 0;
		
		while(true) {
			log("맞춰야 할 숫자는" + targetNumber + "입니다.");
			
			p1.guess();
			p2.guess();
			p3.guess();
			
			guessp1 = p1.number;
			log("1번 선수가 찍은 숫자 : " + guessp1);
			
			guessp2 = p2.number;
			log("2번 선수가 찍은 숫자 : " + guessp2);
			
			guessp3 = p3.number;
			log("3번 선수가 찍은 숫자 : " + guessp3);
			
			
			if (guessp1 == targetNumber) {
				p1isRight = true;
			}
			
			if (guessp2 == targetNumber) {
				p2isRight = true;
			}
			
			if (guessp3 == targetNumber) {
				p3isRight = true;
			}
			
			if (p1isRight || p2isRight || p3isRight) {
				log("맞춘 선수가 있습니다.");
				log("1번 선수는? " + p1isRight);
				log("2번 선수는? " + p2isRight);
				log("3번 선수는? " + p3isRight);
				log("게임이 " + loopCount +"회 만에 끝났습니다.");
				break;
			} else {
				log("다시 해야 합니다.");
				log(" ");
				loopCount++;
			}
			
		}

	}
	
public void log(String msg) {
	System.out.println(msg);
}

}
